import Foundation

/// Enumeration of known types of horizontal text alignment.
/// Get more info: https://www.figma.com/developers/api#typestyle-type
enum FigmaTextHorizontalAlignment: String, Hashable {

    // MARK: - Enumeration Cases

    case left = "LEFT"
    case right = "RIGHT"
    case center = "CENTER"
    case justified = "JUSTIFIED"
}
