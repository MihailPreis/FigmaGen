import Foundation

/// Enumeration of known text cases.
/// Get more info: https://www.figma.com/developers/api#typestyle-type
enum FigmaTextCase: String, Hashable {

    // MARK: - Enumeration Cases

    case original = "ORIGINAL"
    case upper = "UPPER"
    case lower = "LOWER"
    case title = "TITLE"
}
